    private void petugasBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_petugasBtnActionPerformed
        // TODO add your handling code here:
        try {
            dPanel.removeAll();
            dPanel.repaint();
            String levelPetugas = "";
            Petugas p = new Petugas();
            Petugas1 p1 = new Petugas1();

            String sql = "select level from petugas where id_petugas = '" + Data.getUserId() + "'";
            try {
                Statement st = MySqlCon.getConnection().createStatement();
                ResultSet rs = st.executeQuery(sql);

                if (rs.next()) {
                    levelPetugas = rs.getString("level");

                } else {
                    JOptionPane.showMessageDialog(this, "Terdapat kesalahan dalam query!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Query salah " + e);
            }

            if (levelPetugas.equals("Ketua")) {
                dPanel.add(p);
                p.setVisible(true);
                try {
                    p.setMaximum(true);
                } catch (PropertyVetoException ex) {
                    Logger.getLogger(SimpleMenuSwitch.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                dPanel.add(p1);
                p1.setVisible(true);
                try {
                    p1.setMaximum(true);
                } catch (PropertyVetoException ex) {
                    Logger.getLogger(SimpleMenuSwitch.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        } catch (Exception ex) {
            Logger.getLogger(SimpleMenuSwitch.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_petugasBtnActionPerformed

