    public void cek() {

        String sql = "SELECT * FROM petugas where username = '" + tfuser.getText() + "' and passwd = '" + tfpass.getText() + "'";

        try {
            Statement st = MySqlCon.getConnection().createStatement();
            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                if (rs.getString("level").equals("Ketua")) {
                    peminjamanBtn.setEnabled(true);
                    pengembalianBtn.setEnabled(true);
                    peminjamBtn.setEnabled(true);
                    petugasBtn.setEnabled(true);
                    dataBukuBtn.setEnabled(true);
                    kategoriBukuBtn.setEnabled(true);
                    aboutBtn.setEnabled(true);
                    logoutbtn.setEnabled(true);
                    dataBukuBtn.doClick();

                    // this.dispose();
                } else if (rs.getString("level").equals("Petugas")) {

                    peminjamanBtn.setEnabled(true);
                    pengembalianBtn.setEnabled(true);
                    peminjamBtn.setEnabled(true);
                    petugasBtn.setEnabled(true);
                    dataBukuBtn.setEnabled(true);
                    kategoriBukuBtn.setEnabled(true);
                    aboutBtn.setEnabled(true);
                    logoutbtn.setEnabled(true);
                    dataBukuBtn.doClick();
                }
                Data.setNamaPetugas(rs.getString("nama_petugas"));
                Data.setUserId(rs.getString("id_petugas"));
            } else {
                JOptionPane.showMessageDialog(null, "Maaf password atau username anda salah");
                tfuser.setText("");
                tfpass.setText("");
                tfuser.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Query salah \nError kode : " + e);
        }
    }

