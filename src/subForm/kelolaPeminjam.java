    private void showTbPeminjam(String query){
        try{
            Statement st = MySqlCon.getConnection().createStatement();
            try{
                Object[] rows = {"No induk", "Nama siswa", "Jurusan", "Kelas", "Jekel", "Alamat", "Telp"};
                DefaultTableModel  dtm=new DefaultTableModel(null,rows);
                tabelPeminjam.setModel(dtm);
                tabelPeminjam.setBorder(null);
                jScrollPane1.setVisible(true);
                jScrollPane1.setViewportView(tabelPeminjam);
                String induk="", nama="", jurusan="", kelas="", jekel="", alamat="", telp="";
                try{
                    
                    ResultSet rs = st.executeQuery(query);
                    while(rs.next()){
                        induk = rs.getString("no_induk");
                        nama = rs.getString("nama_siswa");
                        jurusan = rs.getString("jurusan");
                        kelas = rs.getString("kelas");
                        jekel = rs.getString("jekel");
                        alamat = rs.getString("alamat");
                        telp = rs.getString("no_telp");
                        
                        String[] row = {""+induk, nama, jurusan, kelas, jekel, alamat, telp};
                        dtm.addRow(row);
                    }
                    
                }
                catch(SQLException en){                
                    JOptionPane.showMessageDialog(null,"Query Salah "+en);
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,"Query Salah "+e);
            }
        }catch(Exception ex){
            Logger.getLogger(kelolaPeminjam.class.getName()).log(Level.SEVERE,null, ex);
        }
    }

