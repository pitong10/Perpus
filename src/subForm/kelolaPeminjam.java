    private void btnGoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGoActionPerformed
        // TODO add your handling code here:
        String[] kelItem = {"X","XI","XII"};
        String[] jurItem = {"TPM", "TKR", "TMM", "TEI", "TAV", "TPL", "TSM", "TBO"};
        String[] jekItem = {"Laki - Laki", "Perempuan"};
        switch(cbKategori.getSelectedItem().toString()){
            case "Kelas":
                
                isiCb.setEnabled(true);
                proceedBtn.setEnabled(true);
                isiCb.removeAllItems();
                for(String itemKel : kelItem){
                    isiCb.addItem(itemKel);
                }
                flag = "kelas";
                break;
            case "Jurusan":
                isiCb.setEnabled(true);
                proceedBtn.setEnabled(true);
                isiCb.removeAllItems();
                for(String itemJur : jurItem){
                    isiCb.addItem(itemJur);
                }
                flag = "jurusan";
                break;
            case "Jenis Kelamin":
                isiCb.setEnabled(true);
                proceedBtn.setEnabled(true);
                isiCb.removeAllItems();
                for(String itemJek : jekItem){
                    isiCb.addItem(itemJek);
                }
                flag = "jekel";
                break;
            default:
                JOptionPane.showInputDialog(this, "Input salah");
        }
    }//GEN-LAST:event_btnGoActionPerformed

