    private void inputBookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputBookBtnActionPerformed

        String sql = "select * from data_buku where kode_buku = '" + bukuTf.getText() + "'";

        try {
            Statement st = MySqlCon.getConnection().createStatement();
            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                if (rs.getString("keterangan").equals("Tersedia")) {

                    String namaBuku = rs.getString("nama_buku");
                    listTa.append(bukuTf.getText() + " - " + namaBuku + "\n");
                    listBuku[index] = bukuTf.getText();
                    index++;
                    bukuTf.setText("");

                } else if (rs.getString("keterangan").equals("Dipinjam")) {
                    JOptionPane.showMessageDialog(this, "Buku telah dipinjam!");
                    bukuTf.setText("");
                }

            } else {
                JOptionPane.showMessageDialog(this, "kode buku salah!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Query salah " + e);
        }
    }//GEN-LAST:event_inputBookBtnActionPerformed

