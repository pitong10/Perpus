    private void cekIdBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cekIdBtnActionPerformed
        // TODO add your handling code here:
        String sql = "select * from peminjam where no_induk='" + noIndukTf.getText() + "'";

        try {
            Statement st = MySqlCon.getConnection().createStatement();
            ResultSet rs = st.executeQuery(sql);

            if (rs.next()) {
                namaSiswaLbl.setText(rs.getString("nama_siswa"));
                submitBtn.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(this, "Nomor induk yang anda inputkan salah!");
                submitBtn.setEnabled(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Query salah " + e);
        }
    }//GEN-LAST:event_cekIdBtnActionPerformed

    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        // TODO add your handling code here:
        String date = todayDate.getText();
        String replace = date.replace("-", "");
        String idTrans = noIndukTf.getText() + "-" + replace + "-" + Data.getUserId() + "-" + kodePinjam;
        JOptionPane.showMessageDialog(this, idTrans);

        String sql = "insert into transaksi_pinjam values('" + idTrans + "','" + noIndukTf.getText() + "','" + Data.getUserId() + "','" + today + "','" + pengembalian + "','" + limit + "')";
        try {
            MySqlCon.ExecuteQuery(sql, "Data peminjaman berhasil dimasukkan");
            noIndukTf.setText("");
            listTa.setText("");
        } catch (Exception ex) {
            Logger.getLogger(inputPeminjaman.class.getName()).log(Level.SEVERE, null, ex);
        }

        String nextSql;
        for (int i = 0; i < index; i++) {
            nextSql = "insert into melibatkan values('" + idTrans + "','" + listBuku[i] + "')";
            try {
                MySqlCon.ExecuteQuery(nextSql, "Data buku yang dipinjam berhasil dimasukkan");
            } catch (Exception ex) {
                Logger.getLogger(inputPeminjaman.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        String updateSql;
        for (int i = 0; i < index; i++) {
            updateSql = "update data_buku set keterangan= 'Dipinjam' where kode_buku = '"+listBuku[i]+"'";
            try {
                MySqlCon.ExecuteQuery(updateSql, "Data buku telah diubah");
            } catch (Exception ex) {
                Logger.getLogger(inputPeminjaman.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        index = 0;
        listBuku = new String[30];
        submitBtn.setEnabled(false);
    }//GEN-LAST:event_submitBtnActionPerformed


