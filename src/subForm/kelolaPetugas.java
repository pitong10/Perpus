    private void tabelPetugasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPetugasMouseClicked
        // TODO add your handling code here:
        try {
            Statement st = MySqlCon.getConnection().createStatement();
            int baris = tabelPetugas.getSelectedRow();
            String klik_tabel = (tabelPetugas.getModel().getValueAt(baris, 0).toString());
            String query = "select * from petugas where id_petugas = '" + klik_tabel + "'";
            String id = "", nama = "", jekel = "", alamat = "", telp = "";
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                id = rs.getString("id_petugas");
                nama = rs.getString("nama_petugas");
                jekel = rs.getString("jekel");
                alamat = rs.getString("alamat");
                telp = rs.getString("telp");

                idLbl.setText(id);
                namaLbl.setText(nama);
                jekelLbl.setText(jekel);
                telpLbl.setText(telp);
                alamatLbl.setText(alamat);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Query salah, error kode : " + e);
        }
    }//GEN-LAST:event_tabelPetugasMouseClicked

