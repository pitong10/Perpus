    private void showTbPetugas(String query) {
        try {
            Statement st = MySqlCon.getConnection().createStatement();
            try {
                Object[] rows = {"ID Petugas", "Nama Petugas", "Jenis Kelamin", "Alamat", "Telp"};
                DefaultTableModel dtm = new DefaultTableModel(null, rows);
                tabelPetugas.setModel(dtm);
                tabelPetugas.setBorder(null);
                jScrollPane2.setVisible(true);
                jScrollPane2.setViewportView(tabelPetugas);
                String id = "", nama = "", jekel = "", alamat = "", telp = "";
                try {

                    ResultSet rs = st.executeQuery(query);
                    while (rs.next()) {
                        id = rs.getString("id_petugas");
                        nama = rs.getString("nama_petugas");
                        jekel = rs.getString("jekel");
                        alamat = rs.getString("alamat");
                        telp = rs.getString("telp");

                        String[] row = {"" + id, nama, jekel, alamat, telp};
                        dtm.addRow(row);
                    }

                } catch (SQLException en) {
                    JOptionPane.showMessageDialog(null, "Query Salah " + en);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Query Salah " + e);
            }
        } catch (Exception ex) {
            Logger.getLogger(kelolaBuku.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

